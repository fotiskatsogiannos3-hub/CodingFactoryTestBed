package gr.aueb.cf.cf9.ch1;

/**
 * Adds two hardcoded integers and prints the result.
 */
public class SumApp {
    public static void main(String[] args) {
        int num1 = 19;
        int num2 = 30;
        int sum = 0;

        sum = num1 + num2;
        System.out.println("The result of the addition is: " + sum);
    }
}
